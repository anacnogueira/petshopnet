 <h1><a name="navegacao">Navegação no site</a></h1>
 
  <h2>Página Inicial</h2>
   <p>Na página inicial de nossa loja, você encontra as principais promoções de
   produtos. Se desejar ver outras promoções, você pode selecionar o link "Mais promoções"
   localizado na parte inferior da página. </p>
  <h2>Busca de produtos</h2>
   <p>Para realizar uma busca de produtos no site, existe o campo de texto localizado
   a área superior a direita em frente a palavra "Busca".É possível realizar
   buscas com o nome inteiro do produto ou parte dele. Quando mais específica for sua
   busca, mas rapidamente você encontrará o que procura. </p>
   <p>Caso sejam encontrados produtos com a palavra chave digitada,
    uma pagina com possíveis resultados é mostrada com foto e nome dos produtos encontrados.
    Há ainda uma terceira opção para adicionar o produto diretamente ao carrinho.</p>
  <h2>Categorias</h2>
   <p>Na area a esquerda do site, você encontra um menu de categorias e subcategorias para
   melhor classificação dos produtos. Clicando em um dos itens desse menu, você será redirecionado
   para uma página com todos os produtos disponíveis nessa categoria ou subcategoria.</p>
  <h2>Informações do produto</h2>
   <p>Na lista de produtos, é possível obter mais detalhes sobre um produto
   clicando sbre o link localizado no nome ou foto do produto.Os itens encontrados nesta página são:</p>
    <ol>
     <li>Nome do produto</li>
     <li>Fabricante</li>
     <li>Imagem</li>
     <li>Botão Comprar</li>
     <li>Descrição do produto</li>
     <li>Formas de pagamento disponíveis</li>
     <li>Avaliar produto</li>
     <li>Comentarios de clientes sobre o produto</li>
     <li>Botão "Escreva um comentário"</li>
    </ol>
    <p>Ao clicar sobre a imagem, é possível visualizar a imagem em tamanho ampliado,
    para melhor visualização do produto.</p>
    <p>Clicando no botão comprar, você será redirecionado para o carrinho,
    onde você encontrará o produto recém adicionado, além de outros
    possíveis produtos já adicionados ao carrinho</p>
    <p>É possível dar nota ao produto clicando nas estrelas localizada abaixo da
    imagem do produto. Quanto mais a esquerda a estrela, menor a nota e quanto
    mais a direita, maior a nota.Cada cliente pode votar apenas uma vez em cada produto.</p>
    <p>Você pode ver a avaliação dos clientes sobre o produto baixo do item Comentários.
