   de preenchimento obrigatórios. Alguns campos exemplificam a forma como o campo deve ser preenchido, com um texto
   explicativo localizado à direita do campo. Após o cadastro dos dados necessários, clique no botão "enviar" para
   que você possa começar a realizar compras no site.</p>
