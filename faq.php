 <h1><a name="compras">Fazendo compras</a></h1>
  <h2>Escolha o produto</h2>
  <p>Você pode escolher um ou mais produtos através da vitrine principal, pela categorias localizadas
  no canto esquerdo da tela ou fazendo uma busca. Localizado o produto em uma das 3 formas anteriores,
  você pode clicar sobre o nome ou imagem do produto para obter mais detalhes do produto, como um breve resumo das características
  do produtos e fotos. </p>
  <h2>Coloque produtos no carrinho</h2>
  <p>Para adicionar automaticamente um produto ao carrinho, você pode clicar diretamente no link Adicionar ao carrinho,
  localizado na vitrine do produtos, abaixo do nome do produto desejado. Existe outra alternativa de adicionar
  produtos ao carrinho  de um produto, na pagina de detalhes do produto, clique no botão comprar
  logo abaixo da imagem  do carrinho de compras, você redirecionado para o carrinho de compras.
  No carrinho de compras você pode visualizar os produtos ja inseridos, acrescentar ou retirar produtos, modificar a qtde
  de um produto, calcular frete, estimar prazos de entrega e escolher a forma de pagamento.</p>
  <h2>Calcule valor do frete e prazo de entrega </h2>
  <p>No carrinho de compras, para prosseguir com a compra de um mais produtos, é necessario
  fazer o calculo do frete do endereço de entrega. Para fazer este calculo, você deve inserir
  seu CEP em frente a frase "Digite o CEP do endereço de entrega para calcular o valor do frete",
  no seguinte formato: 99999-999 (cinco numeros de 0 a 9 seguidos de 3 numeros de 0 a 9).
 Depois de inserir o CEP, clique no botão Ok em frete ao campo de CEP. Ao clicar no botão de frete,
 o valor do frete será mostrado a baixo da lista de produtos, seguidos abaixo do valor total da compra
 com frete e logo baixo o prazo estimado de entrega.</p>
